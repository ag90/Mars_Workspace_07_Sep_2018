package OOP;

public class ConcatenationConcept {
	

	public static void main(String[] args) {


		int a=10;
		int b=20;
		String Test="Selenium";
		String Java="Interface";
		
		System.out.println(a+b);
		
		System.out.println(Test+Java);
		
		System.out.println(a+Test);//This is concatenation, when execution always starts from left to right.
		
		System.out.println(b+Java);//This is concatenation, when execution always starts from left to right.
		
		System.out.println(Test+a);//This is concatenation, when execution always starts from left to right.
		
		System.out.println(a+b+Test+Java+a+b+a+b);//This is concatenation, when execution always starts from left to right.
		
		System.out.println(Test+a+b);//(concatenation)If execution start from string after that integer with out bracket both will be merge, not added.
		
		System.out.println(a+Test+b+Java+a+b);//10Selenium20Interface1020
		
		System.out.println(Test+a+b+Java+b+a+Test+a);//Selenium1020Interface2010Selenium10
		
		System.out.println(Test+a+b+a+b+Java);//Selenium10201020Interface
		
		System.out.println(a+b+Test+a+b+a+b+Java);//30Selenium10201020Interface
		
		System.out.println(Test+Java+(a+b));//This is used to addtion.
		
	}

}
